const randomGrid = (width, height) => {
  const cells = [ '.', ' ' ];

  let grid = [];

  for (let y = 0; y <= height; y++) {
    grid[y] = [];

    for (let x = 0; x <= width; x++) {
      const rand = cells[Math.floor(Math.random() * cells.length)];
      grid[y].push(rand);
    }
  }

  return grid;
}

